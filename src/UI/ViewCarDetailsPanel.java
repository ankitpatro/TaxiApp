    private void viewDetails() {
        txtAvailability.setText(car.isIsAvailable() ? "Yes" : "No");
        txtAvailableCity.setText(car.getAvailableCity());
        txtMaintenanceCertificate.setText(car.hasMaintenanceCertificateExpired() ? "Yes" : "No");
        txtManufacturingYear.setText(String.valueOf(car.getManufacturedYear()));
        txtMaxSeats.setText(String.valueOf(car.getMaxSeats()));
        txtMinSeats.setText(String.valueOf(car.getMinSeats()));
        txtModel.setText(car.getModel());
        txtModelNumber.setText(car.getModelNumber());
        txtSerialNum.setText(String.valueOf(car.getSerial_num()));

        btnSave.setEnabled(false);
    }
