    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
       
        lowerPanel.remove(this);
        
        CardLayout layout = (CardLayout) lowerPanel.getLayout();
        Component[] comps = lowerPanel.getComponents();
        for (Component component : comps) {
            if (component instanceof ManageCarDetailsPanel) {
                ManageCarDetailsPanel manageCarDetailsPanel = (ManageCarDetailsPanel) component;
                manageCarDetailsPanel.populateTable(carInformationList.getCarList());
                manageCarDetailsPanel.populateUpdateDatTime(carInformationList);
            }
        }
        
        layout.previous(lowerPanel);
        
        
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        btnSave.setEnabled(true);
        btnUpdate.setEnabled(false);
        txtAvailability.setVisible(false);
        txtAvailableCity.setEnabled(true);
        txtMaintenanceCertificate.setVisible(false);
        txtManufacturingYear.setEnabled(true);
        txtMaxSeats.setEnabled(true);
        txtMinSeats.setEnabled(true);
        txtModel.setEnabled(true);
        txtModelNumber.setEnabled(true);
        txtSerialNum.setEnabled(true);

        chkIsAvailable.setVisible(true);
        chkHasMainCertExp.setVisible(true);

        if (txtAvailability.getText().equalsIgnoreCase("yes")) {
            chkIsAvailable.setSelected(true);
        } else {
            chkIsAvailable.setSelected(false);
        }
        if (txtMaintenanceCertificate.getText().equalsIgnoreCase("yes")) {
            chkHasMainCertExp.setSelected(true);
        } else {
            chkHasMainCertExp.setSelected(false);
        }


    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);

        txtAvailability.setVisible(true);
        txtMaintenanceCertificate.setVisible(true);

        chkHasMainCertExp.setVisible(false);
        chkIsAvailable.setVisible(false);

        txtAvailability.setEnabled(false);
        txtAvailableCity.setEnabled(false);
        txtMaintenanceCertificate.setEnabled(false);
        txtManufacturingYear.setEnabled(false);
        txtMaxSeats.setEnabled(false);
        txtMinSeats.setEnabled(false);
        txtModel.setEnabled(false);
        txtModelNumber.setEnabled(false);
        txtSerialNum.setEnabled(false);

        if (chkHasMainCertExp.isSelected()) {
            txtMaintenanceCertificate.setText("Yes");
        } else {
            txtMaintenanceCertificate.setText("No");
        }

        if (chkIsAvailable.isSelected()) {
            txtAvailability.setText("Yes");
        } else {
            txtAvailability.setText("No");
        }

        car.setIsAvailable(chkIsAvailable.isSelected());
        car.setAvailableCity(txtAvailableCity.getText());
        car.setHasMaintenanceCertificateExpired(chkHasMainCertExp.isSelected());
        try {

            car.setManufacturedYear(Integer.parseInt(txtManufacturingYear.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter valid year");
        }

        try {
            car.setMaxSeats(Integer.parseInt(txtMaxSeats.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Enter valid number of seats");
        }

        try {
            car.setMinSeats(Integer.parseInt(txtMinSeats.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Enter valid number of seats");
        }

        try {
            car.setSerial_num(Integer.parseInt(txtSerialNum.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Enter valid number of serial number");
        }

        car.setModel(txtModel.getText());
        car.setModelNumber(txtModelNumber.getText());
        
        carInformationList.setLastUpdateTime(LocalDateTime.now());
    }//GEN-LAST:event_btnSaveActionPerformed


